SELECT C.CUSTOMER_ID,C.CUSTOMER_NAME,O.ORDER_ID FROM CUSTOMERS C   
INNER JOIN ORDERS O ON C.CUSTOMER_ID = O.CUSTOMER_ID;

SELECT C.CUSTOMER_NAME ,O.ORDER_ID FROM ORDERS O 
INNER JOIN CUSTOMERS C ON C.CUSTOMER_ID=O.CUSTOMER_ID;

SELECT P.PRODUCT_ID,P.PRODUCT_NAME,C.CATEGORY_ID,C.CATEGORY_NAME FROM PRODUCTS P
LEFT JOIN CATEGORIES C ON P.CATEGORY_ID=C.CATEGORY_ID;

SELECT P.PRODUCT_ID,P.PRODUCT_NAME,S.SUPPLIER_NAME FROM PRODUCTS P 
INNER JOIN  SUPPLIERS S ON P.SUPPLIER_ID = S.SUPPLIER_ID;

SELECT O.ORDER_ID,E.EMPLOYEE_ID FROM ORDERS O 
LEFT JOIN EMPLOYEES E ON O.EMPLOYEE_ID=E.EMPLOYEE_ID;

SELECT O.ORDER_ID,S.SHIPPER_NAME FROM ORDERS O 
RIGHT JOIN SHIPPERS S ON O.SHIPPER_ID = S.SHIPPER_ID;

SELECT C.CUSTOMER_ID,COUNT(O.ORDER_ID) AS TOTAL_ORDERS FROM CUSTOMERS C 
INNER JOIN ORDERS O ON C.CUSTOMER_ID=O.CUSTOMER_ID
GROUP BY C.CUSTOMER_ID
HAVING COUNT(O.ORDER_ID)>=1;

SELECT O.ORDER_ID,P.PRODUCT_NAME,QI.QUANTITY FROM ORDERS O 
JOIN ORDERITEMS QI ON O.ORDER_ID=QI.ORDER_ID JOIN PRODUCTS P 
ON QI.PRODUCT_ID-P.PRODUCT_ID ORDER BY O.ORDER_ID;

SELECT P.PRODUCT_NAME ,OI.QUANTITY FROM PRODUCTS P 
JOIN ORDERITEMS OI ON P.PRODUCT_ID=OI.PRODUCT_ID;

SELECT E.EMPLOYEE_NAME,C.CUSTOMER_NAME FROM ORDERS O 
JOIN EMPLOYEES E ON O.EMPLOYEE_ID=E.EMPLOYEE_ID
JOIN CUSTOMERS C ON O.CUSTOMER_ID=C.CUSTOMER_ID;

SELECT C.CUSTOMER_NAME,O.ORDER_ID,P.PRODUCT_NAME,OI.QUANTITY,P.PRICE FROM CUSTOMERS C JOIN ORDERS O 
ON C.CUSTOMER_ID=O.CUSTOMER_ID JOIN ORDERITEMS OI ON O.ORDER_ID=OI.ORDER_ID
JOIN PRODUCTS P ON OI.PRODUCT_ID = P.PRODUCT_ID;

SELECT S.SUPPLIER_NAME,P.PRODUCT_NAME FROM SUPPLIERS S
JOIN PRODUCTS P ON S.SUPPLIER_ID = P.SUPPLIER_ID
JOIN ORDERITEMS OI ON P.PRODUCT_ID = OI.PRODUCT_ID;

SELECT C.CATEGORY_NAME,COUNT(P.PRODUCT_ID) AS PRODUCT_COUNT
FROM CATEGORIES C LEFT JOIN PRODUCTS P
ON C.CATEGORY_ID = P.CATEGORY_ID GROUP BY C.CATEGORY_NAME;

SELECT C.CUSTOMER_NAME,COUNT(O.ORDER_ID) AS TOTAL_ORDERS
FROM CUSTOMERS C LEFT JOIN ORDERS O
ON C.CUSTOMER_ID = O.CUSTOMER_ID GROUP BY C.CUSTOMER_NAME;

SELECT E.EMPLOYEE_NAME,SUM(OI.QUANTITY * P.PRICE) AS TOTAL_SALES FROM EMPLOYEES E
JOIN ORDERS O ON E.EMPLOYEE_ID = O.EMPLOYEE_ID
JOIN ORDERITEMS OI ON O.ORDER_ID = OI.ORDER_ID
JOIN PRODUCTS P ON OI.PRODUCT_ID = P.PRODUCT_ID
GROUP BY E.EMPLOYEE_NAME;

SELECT C.CATEGORY_NAME,SUM(OI.QUANTITY * P.PRICE) AS REVENUE
FROM CATEGORIES C
JOIN PRODUCTS P ON C.CATEGORY_ID = P.CATEGORY_ID
JOIN ORDERITEMS OI ON P.PRODUCT_ID = OI.PRODUCT_ID
GROUP BY C.CATEGORY_NAME;

SELECT C.CATEGORY_NAME,P.PRODUCT_NAME,P.PRICE FROM PRODUCTS P
JOIN CATEGORIES C ON P.CATEGORY_ID = C.CATEGORY_ID
WHERE P.PRICE =(SELECT MAX(P2.PRICE)FROM PRODUCTS P2
WHERE P2.CATEGORY_ID = P.CATEGORY_ID);

SELECT P.PRODUCT_NAME FROM PRODUCTS P
LEFT JOIN ORDERITEMS OI ON P.PRODUCT_ID = OI.PRODUCT_ID
WHERE OI.PRODUCT_ID IS NULL;

SELECT C.CUSTOMER_NAME FROM CUSTOMERS C
LEFT JOIN ORDERS O ON C.CUSTOMER_ID = O.CUSTOMER_ID
WHERE O.ORDER_ID IS NULL;

SELECT S.SUPPLIER_NAME FROM SUPPLIERS S
LEFT JOIN PRODUCTS P ON S.SUPPLIER_ID = P.SUPPLIER_ID
WHERE P.PRODUCT_ID IS NULL;

SELECT C.CUSTOMER_NAME,SUM(OI.QUANTITY * P.PRICE) AS SPENDING FROM CUSTOMERS C
JOIN ORDERS O ON C.CUSTOMER_ID = O.CUSTOMER_ID
JOIN ORDERITEMS OI ON O.ORDER_ID = OI.ORDER_ID
JOIN PRODUCTS P ON OI.PRODUCT_ID = P.PRODUCT_ID
GROUP BY C.CUSTOMER_NAME
ORDER BY SPENDING DESC
LIMIT 5;

SELECT E.EMPLOYEE_NAME,MONTH(O.ORDER_DATE) AS MONTH,SUM(OI.QUANTITY * P.PRICE) AS SALES
FROM EMPLOYEES E
JOIN ORDERS O ON E.EMPLOYEE_ID = O.EMPLOYEE_ID
JOIN ORDERITEMS OI ON O.ORDER_ID = OI.ORDER_ID
JOIN PRODUCTS P ON OI.PRODUCT_ID = P.PRODUCT_ID
GROUP BY E.EMPLOYEE_NAME, MONTH(O.ORDER_DATE);

SELECT P.PRODUCT_NAME,SUM(OI.QUANTITY) AS TOTAL_SOLD FROM PRODUCTS P
JOIN ORDERITEMS OI ON P.PRODUCT_ID = OI.PRODUCT_ID
GROUP BY P.PRODUCT_NAME
ORDER BY TOTAL_SOLD DESC
LIMIT 1;

SELECT S.SUPPLIER_NAME,SUM(OI.QUANTITY * P.PRICE) AS REVENUE FROM SUPPLIERS S
JOIN PRODUCTS P ON S.SUPPLIER_ID = P.SUPPLIER_ID
JOIN ORDERITEMS OI ON P.PRODUCT_ID = OI.PRODUCT_ID
GROUP BY S.SUPPLIER_NAME;

SELECT C.CUSTOMER_NAME,AVG(OI.QUANTITY * P.PRICE) AS AVG_ORDER_VALUE FROM CUSTOMERS C
JOIN ORDERS O ON C.CUSTOMER_ID = O.CUSTOMER_ID
JOIN ORDERITEMS OI ON O.ORDER_ID = OI.ORDER_ID
JOIN PRODUCTS P ON OI.PRODUCT_ID = P.PRODUCT_ID
GROUP BY C.CUSTOMER_NAME;

SELECT ORDER_ID,COUNT(PRODUCT_ID) AS PRODUCT_COUNT FROM ORDERITEMS
GROUP BY ORDER_ID
HAVING COUNT(PRODUCT_ID) > 3;

SELECT C.CUSTOMER_NAME,COUNT(DISTINCT P.CATEGORY_ID) AS CATEGORY_COUNT FROM CUSTOMERS C
JOIN ORDERS O ON C.CUSTOMER_ID = O.CUSTOMER_ID
JOIN ORDERITEMS OI ON O.ORDER_ID = OI.ORDER_ID
JOIN PRODUCTS P ON OI.PRODUCT_ID = P.PRODUCT_ID
GROUP BY C.CUSTOMER_NAME
HAVING COUNT(DISTINCT P.CATEGORY_ID) > 1;

SELECT C.CATEGORY_NAME FROM CATEGORIES C
LEFT JOIN PRODUCTS P ON C.CATEGORY_ID = P.CATEGORY_ID
LEFT JOIN ORDERITEMS OI ON P.PRODUCT_ID = OI.PRODUCT_ID
WHERE OI.PRODUCT_ID IS NULL;

SELECT O.ORDER_ID,SUM(OI.QUANTITY * P.PRICE) AS ORDER_VALUE FROM ORDERS O
JOIN ORDERITEMS OI ON O.ORDER_ID = OI.ORDER_ID
JOIN PRODUCTS P ON OI.PRODUCT_ID = P.PRODUCT_ID
GROUP BY O.ORDER_ID
ORDER BY ORDER_VALUE DESC
LIMIT 1;

SELECT C.CUSTOMER_NAME,
       O.ORDER_ID,
       O.ORDER_DATE,
       E.EMPLOYEE_NAME,
       S.SHIPPER_NAME,
       P.PRODUCT_NAME,
       CA.CATEGORY_NAME,
       SU.SUPPLIER_NAME,
       OI.QUANTITY,
       P.PRICE,
       (OI.QUANTITY * P.PRICE) AS TOTAL_AMOUNT
FROM CUSTOMERS C
JOIN ORDERS O
ON C.CUSTOMER_ID = O.CUSTOMER_ID
JOIN EMPLOYEES E
ON O.EMPLOYEE_ID = E.EMPLOYEE_ID
JOIN SHIPPERS S
ON O.SHIPPER_ID = S.SHIPPER_ID
JOIN ORDERITEMS OI
ON O.ORDER_ID = OI.ORDER_ID
JOIN PRODUCTS P
ON OI.PRODUCT_ID = P.PRODUCT_ID
JOIN CATEGORIES CA
ON P.CATEGORY_ID = CA.CATEGORY_ID
JOIN SUPPLIERS SU
ON P.SUPPLIER_ID = SU.SUPPLIER_ID
ORDER BY O.ORDER_ID;