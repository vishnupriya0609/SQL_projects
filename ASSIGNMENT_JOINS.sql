CREATE DATABASE E_COMMERCE_DB;
USE E_COMMERCE_DB;

CREATE TABLE CUSTOMERS (                            -- 
    CUSTOMER_ID INT PRIMARY KEY,
    CUSTOMER_NAME VARCHAR(50) NOT NULL,     
    PHONE VARCHAR(15),
    CITY VARCHAR(20) DEFAULT 'CHENNAI'
);

CREATE TABLE EMPLOYEES (                      -- EMPLOYEES
    EMPLOYEE_ID INT PRIMARY KEY,
    EMPLOYEE_NAME VARCHAR(50) NOT NULL,
    POSITION VARCHAR(30),
    MANAGER_ID INT,
    SALARY DECIMAL(10,2),
    FOREIGN KEY (MANAGER_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID)
);

CREATE TABLE SHIPPERS (                         -- SHIPPERS
    SHIPPER_ID INT PRIMARY KEY,
    SHIPPER_NAME VARCHAR(50),
    PHONE VARCHAR(15)
);

CREATE TABLE CATEGORIES (                       -- CATEGORIES
    CATEGORY_ID INT PRIMARY KEY,
    CATEGORY_NAME VARCHAR(50) NOT NULL
);

CREATE TABLE SUPPLIERS (                       -- SUPPLIERS
    SUPPLIER_ID INT PRIMARY KEY,
    SIPPLIER_NAME VARCHAR(50) NOT NULL,
    CITY VARCHAR(50),
    COUNTRY VARCHAR(40)
);

CREATE TABLE PRODUCTS (                       -- PRODUCTS
    PRODUCT_ID INT PRIMARY KEY,
    PRODUCT_NAME VARCHAR(50) NOT NULL,
    CATEGORY_ID INT,
    SUPPLIER_ID INT,
    PRICE DECIMAL(10,2),
    STOCK INT,
    FOREIGN KEY (CATEGORY_ID) REFERENCES CATEGORIES(CATEGORY_ID),
    FOREIGN KEY (SUPPLIER_ID) REFERENCES SUPPLIERS(SUPPLIER_ID)
);
CREATE TABLE ORDERS (                   -- ORDERS
    ORDER_ID INT PRIMARY KEY,
    CUSTOMER_ID INT,
    EMPLOYEE_ID INT,
    SHIPPER_ID INT,
    ORDER_DATE DATE,
    Status VARCHAR(30),
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS(CUSTOMER_ID),
    FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES(EMPLOYEE_ID),
    FOREIGN KEY (SHIPPER_ID) REFERENCES SHIPPERS(SHIPPER_ID)
);

CREATE TABLE ORDERITEMS (                            -- ORDER ITEMS
    ORDERITEM_ID INT PRIMARY KEY,
    ORDER_ID INT,
    PRODUCT_ID INT,
    QUANTITY INT,
    FOREIGN KEY (ORDER_ID) REFERENCES ORDERS(ORDER_ID),
    FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS(PRODUCT_ID)
);

